uint32_t bb_led_gamma_cie(uint16_t level)
{
    // Map level (0..65535) onto the 257-entry LUT with linear interpolation.
    // p = level*256 spans 0..256*65535; idx is the lower LUT entry, rem the
    // fractional offset (0..65534) into the segment to the next entry.
    uint32_t p   = (uint32_t)level * 256u;
    uint32_t idx = p / 65535u;            // 0..256
    uint32_t rem = p % 65535u;            // 0..65534
    uint32_t lo  = s_cie_lut[idx];
    uint32_t hi  = s_cie_lut[idx < 256u ? idx + 1u : 256u];
    return lo + (hi - lo) * rem / 65535u;
}
