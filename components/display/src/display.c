static esp_lcd_panel_handle_t panel_handle = NULL;
static uint16_t *framebuffer = NULL;
static size_t framebuffer_size = 0;

// Helper: convert RGB888 to RGB565
static uint16_t rgb888_to_rgb565(uint8_t r, uint8_t g, uint8_t b) {
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}

// Helper: plot a single pixel in the framebuffer
static void plot_pixel(int x, int y, uint16_t color) {
    if (x >= 0 && x < PANEL_WIDTH && y >= 0 && y < PANEL_HEIGHT) {
        framebuffer[y * PANEL_WIDTH + x] = color;
    }
}

// Helper: draw a single 8x8 character bitmap at (x, y) with given color and bg color
static void draw_char_bitmap(int x, int y, const uint8_t *bitmap, uint16_t fg, uint16_t bg) {
    for (int row = 0; row < 8; row++) {
        uint8_t bits = bitmap[row];
        for (int col = 0; col < 8; col++) {
            uint16_t color = (bits & (0x80 >> col)) ? fg : bg;
            plot_pixel(x + col, y + row, color);
        }
    }
}

bsp_display_err_t bsp_display_init(void) {
    esp_err_t ret;

    // Allocate framebuffer
    framebuffer_size = PANEL_WIDTH * PANEL_HEIGHT * sizeof(uint16_t);
    framebuffer = malloc(framebuffer_size);
    if (!framebuffer) {
        return ESP_ERR_NO_MEM;
    }
    memset(framebuffer, 0, framebuffer_size);

    // Acquire LDO channel 3 for MIPI DPHY @ 2500 mV
    esp_ldo_channel_handle_t ldo_ch;
    esp_ldo_channel_config_t ldo_cfg = {
        .chan_id = LDO_CHANNEL,
        .voltage_mv = LDO_VOLTAGE_MV,
    };
    ret = esp_ldo_acquire_channel(&ldo_cfg, &ldo_ch);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Create MIPI-DSI bus with 2 lanes @ 900 Mbps
    esp_lcd_dsi_bus_handle_t dsi_bus;
    esp_lcd_dsi_bus_config_t dsi_bus_cfg = {
        .bus_id = 0,
        .num_data_lanes = PANEL_LANES,
        .phy_clk_src = MIPI_DSI_PHY_CLK_SRC_DEFAULT,
        .lane_bit_rate_mbps = PANEL_LANE_SPEED_MBPS,
    };
    ret = esp_lcd_new_dsi_bus(&dsi_bus_cfg, &dsi_bus);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Create DPI panel with RGB565, 1024x600, 51 MHz DPI clock
    esp_lcd_dpi_panel_config_t dpi_cfg = {
        .dpi_clk_src = MIPI_DSI_DPI_CLK_SRC_DEFAULT,
        .dpi_clock_freq_mhz = DPI_CLK_MHZ,
        .pixel_format = LCD_COLOR_SPACE_RGB,
        .data_width = 16,  // RGB565
        .psram_trans_align = 64,
        .frame_buffer = framebuffer,
        .frame_buffer_size = framebuffer_size,
        .hsync_idle_low = false,
        .vsync_idle_low = false,
        .de_idle_high = false,
        .pclk_idle_high = false,
        .disp_id = 0,
        .color_type = LCD_COLOR_SPACE_RGB,
    };
    ret = esp_lcd_new_rgb_panel(&dpi_cfg, &panel_handle);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Configure panel IO via DBI (DCS command interface)
    esp_lcd_panel_io_handle_t io_handle;
    esp_lcd_dbi_io_config_t dbi_cfg = {
        .bus_id = 0,
        .lcd_clk_src = MIPI_DSI_DPI_CLK_SRC_DEFAULT,
        .clock_freq_mhz = DPI_CLK_MHZ,
    };
    ret = esp_lcd_new_panel_io_dbi(dsi_bus, &dbi_cfg, &io_handle);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Add EK79007 panel driver with built-in vendor init commands
    esp_lcd_panel_dev_config_t panel_cfg = {
        .reset_gpio_num = -1,  // No discrete reset line; use DCS SWRESET
        .color_space = LCD_COLOR_SPACE_RGB,
        .bits_per_pixel = 16,
        .vendor_config = NULL,
    };
    ret = esp_lcd_new_panel_ek79007(io_handle, &panel_cfg, &panel_handle);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Initialize panel
    ret = esp_lcd_panel_init(panel_handle);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Setup backlight PWM on GPIO31: 30 kHz, 10-bit resolution
    ledc_timer_config_t ledc_timer = {
        .speed_mode = LEDC_HIGH_SPEED_MODE,
        .timer_num = LEDC_TIMER_0,
        .duty_resolution = LEDC_TIMER_10_BIT,
        .freq_hz = 30000,
        .clk_cfg = LEDC_AUTO_CLK,
    };
    ret = ledc_timer_config(&ledc_timer);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    ledc_channel_config_t ledc_channel = {
        .speed_mode = LEDC_HIGH_SPEED_MODE,
        .channel = LEDC_CHANNEL_0,
        .timer_sel = LEDC_TIMER_0,
        .intr_type = LEDC_INTR_DISABLE,
        .gpio_num = PIN_BL_PWM,
        .duty = 1023,  // Max brightness (10-bit)
        .hpoint = 0,
    };
    ret = ledc_channel_config(&ledc_channel);
    if (ret != ESP_OK) {
        free(framebuffer);
        return ret;
    }

    // Turn on backlight power enable GPIO
    gpio_set_direction(PIN_BL_PWR, GPIO_MODE_OUTPUT);
    gpio_set_level(PIN_BL_PWR, 1);

    // Clear display to black and sync
    bsp_display_clear(0x0000);

    return ESP_OK;
}

void bsp_display_clear(uint16_t rgb565) {
    if (!framebuffer) {
        return;
    }
    for (size_t i = 0; i < (framebuffer_size / sizeof(uint16_t)); i++) {
        framebuffer[i] = rgb565;
    }
    // Sync to panel
    if (panel_handle) {
        esp_lcd_panel_draw_bitmap(panel_handle, 0, 0, PANEL_WIDTH, PANEL_HEIGHT, (uint8_t *)framebuffer);
    }
}

void bsp_display_draw_text(int x, int y, const char *text) {
    if (!text || !framebuffer) {
        return;
    }

    // White text on transparent (existing framebuffer content)
    uint16_t fg_color = rgb888_to_rgb565(255, 255, 255);

    int cur_x = x;
    int cur_y = y;
    const char *p = text;

    while (*p) {
        unsigned char c = (unsigned char)(*p);

        // Wrap to next line if at edge
        if (cur_x + 8 > PANEL_WIDTH) {
            cur_x = x;
            cur_y += 8;
        }

        // Stop if off bottom
        if (cur_y + 8 > PANEL_HEIGHT) {
            break;
        }

        // Draw character
        if (c < 128) {
            draw_char_bitmap(cur_x, cur_y, font_8x8[c], fg_color, 0x0000);
        }

        cur_x += 8;
        p++;
    }

    // Sync to panel
    if (panel_handle) {
        esp_lcd_panel_draw_bitmap(panel_handle, 0, 0, PANEL_WIDTH, PANEL_HEIGHT, (uint8_t *)framebuffer);
    }
}

void bsp_display_show_splash(const char *product, const char *version) {
    if (!product || !version) {
        return;
    }

    // Clear to black
    bsp_display_clear(0x0000);

    // Center text: product name at vertical center, version below
    int product_y = (PANEL_HEIGHT / 2) - 20;
    int version_y = product_y + 16;

    // Simple centering approximation: assume 8-pixel-wide chars
    int product_len = strlen(product);
    int product_x = (PANEL_WIDTH - (product_len * 8)) / 2;
    if (product_x < 0) product_x = 0;

    int version_len = strlen(version);
    int version_x = (PANEL_WIDTH - (version_len * 8)) / 2;
    if (version_x < 0) version_x = 0;

    bsp_display_draw_text(product_x, product_y, product);
    bsp_display_draw_text(version_x, version_y, version);
}

void bsp_display_show_prov(const char *ap_ssid, const char *ap_pass) {
    if (!ap_ssid || !ap_pass) {
        return;
    }

    // Clear to black
    bsp_display_clear(0x0000);

    // Simple provisioning layout: label + SSID on one line, label + password on next
    int y = 50;
    bsp_display_draw_text(10, y, "SSID:");
    bsp_display_draw_text(60, y, ap_ssid);

    y += 20;
    bsp_display_draw_text(10, y, "Pass:");
    bsp_display_draw_text(60, y, ap_pass);
}

void bsp_display_off(void) {
    if (!panel_handle) {
        return;
    }

    // Turn off backlight PWM
    ledc_set_duty(LEDC_HIGH_SPEED_MODE, LEDC_CHANNEL_0, 0);
    ledc_update_duty(LEDC_HIGH_SPEED_MODE, LEDC_CHANNEL_0);

    // Turn off backlight power
    gpio_set_level(PIN_BL_PWR, 0);

    // Put panel to sleep via DCS command
    esp_lcd_panel_disp_off(panel_handle);
}
