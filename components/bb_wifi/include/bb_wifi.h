// Inject the OTA-image-validated query bb_wifi's cold-boot timeout / persistent-
// disconnect safeguard / retry-forever gates read to decide whether the running
// image is "trusted" (skip cold-boot reboot, retry indefinitely, don't bump
// boot_count toward AP-fallback). Composition-only — bb_wifi does not self-
// register a default and has no dependency on bb_ota_validator. Pass NULL to
// clear the callback. When no callback is set, bb_wifi treats the running
// image as trusted (returns true) — a build without bb_ota_validator has no
// rollback watchdog at all, so the running image is implicitly permanent and
// every one of these gates should behave exactly as it did when the direct
// bb_ota_is_validated() call always found a validated image. MUST be called
// before bb_wifi's EARLY-tier init runs (bb_app_init_early() under codegen,
// or the equivalent hand-wired call), whichever composition path is in use,
// so it is set before the EARLY-tier bb_wifi_autoinit runs.
// Setter + public invoke generated by the bb_core "single-slot injected
// callback" macro (bb_callback_slot.h, BB_CALLBACK_SLOT_RET), instantiated
// in platform/host/bb_wifi/bb_wifi_emit.c (host+esp compiled) as
// bb_wifi_internal_ota_validated(); NULL cb -> invoke returns true (see the
// "implicitly permanent" rationale above). invoke reads its own file-static
// slot (no separate dispatch(cb, ...) symbol) -- exercised via the real
// shipped setter/invoke pair by test/test_host/test_bb_wifi.c; the macro's
// generic fire/no-fire logic is covered once, in isolation, by bb_core's
// test/test_host/test_bb_callback_slot.c.
typedef bool (*bb_wifi_ota_validated_fn)(void);
