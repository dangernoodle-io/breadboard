// ---------------------------------------------------------------------------
// Net-event edges (KB 781) — the three STA lifecycle edges published via the
// generic emit seam below (bb_wifi_set_emit / BB_WIFI_EVENT_TOPIC).
// ---------------------------------------------------------------------------

typedef enum {
    BB_WIFI_NET_EVT_GOT_IP = 0,
    BB_WIFI_NET_EVT_DISCONNECT,
    BB_WIFI_NET_EVT_LOST_IP,
    BB_WIFI_NET_EVT_REBOOT_DENIED, // B1-805: reconnect FSM escalation denied
                                    // (budget/boot-fail-count) -- fell back to
                                    // backoff instead of rebooting.
} bb_wifi_net_event_t;

// The reason is passed explicitly as a call argument -- NOT read back via
// bb_wifi_get_disconnect() from inside the emit callback. Reading it back
// races wifi_reconn_on_disconnect() (which updates the async disconnect-reason
// state AFTER bb_wifi_publish_net_event() fires for BB_WIFI_NET_EVT_DISCONNECT
// in the WIFI_EVENT_STA_DISCONNECTED handler), so a callback calling
// bb_wifi_get_disconnect() would observe a stale value. Passing the reason
// through the call closes that race.
//
// Per-edge value:
//   BB_WIFI_NET_EVT_GOT_IP        -> BB_WIFI_DISC_UNKNOWN (not meaningful)
//   BB_WIFI_NET_EVT_DISCONNECT    -> the mapped reason for this disconnect
//                                    (bb_wifi_map_esp_reason() of the esp_wifi
//                                    WIFI_REASON_* code)
//   BB_WIFI_NET_EVT_LOST_IP       -> BB_WIFI_DISC_BB_LOST_IP
//   BB_WIFI_NET_EVT_REBOOT_DENIED -> the last disconnect reason (mapped),
//                                    or BB_WIFI_DISC_UNKNOWN when the denial
//                                    was on a connect-timeout stall (no
//                                    disconnect reason code applies)
//
// `evt` is the discriminator, not `reason` alone: BB_WIFI_DISC_UNKNOWN is
// overloaded -- on GOT_IP it's a "not applicable" filler (there is no
// disconnect), on DISCONNECT it's a genuine disconnect whose ESP reason
// code isn't in bb_wifi_map_esp_reason()'s table. A consumer that buckets
// purely on `reason` (e.g. metrics/logging) without also keying on `evt`
// will conflate those two populations.

// ---------------------------------------------------------------------------
// wifi.net event contract (KB 820, PR1) + generic emit seam (PR3)
// ---------------------------------------------------------------------------
// bb_wifi stays bb_event-FREE (no bb_event include, no bb_event CMakeLists
// dependency) -- but it publishes this payload itself, via a single-slot
// seam: bb_wifi_set_emit(bb_emit_fn) (bb_emit.h, bb_core). That slot is
// bus-shaped ((topic, id, payload, size), no bb_event types) so composition
// can wire it directly to bb_event_emit without bb_wifi ever including
// bb_event -- the caller supplies the emit sink.
// bb_wifi_emit_baseline() additionally lets a late-registering consumer
// request a one-shot synthesis of the CURRENT state onto the emit slot (see
// its declaration below) -- the wifi.net contract's NO RETAIN/REPLAY note
// (below) is otherwise unaffected: a late bb_event subscriber still must
// call bb_wifi_has_ip()/bb_wifi_get_info() itself before subscribing, since
// baseline-post only helps a consumer that registers before subscribing,
// not one that subscribes to bb_event directly.
//
// Topic: "wifi.net" -- distinct from BB_TOPIC_WIFI ("wifi") above, which is
// reserved for the bb_cache/HTTP snapshot (GET /api/wifi). Do not reuse
// BB_TOPIC_WIFI for the event stream.
#define BB_WIFI_EVENT_TOPIC "wifi.net"

// Payload published on BB_WIFI_EVENT_TOPIC. The bb_wifi_net_event_t edge
// (GOT_IP/DISCONNECT/LOST_IP) is carried as the bb_event id (0/1/2) --
// id-as-discriminator, the same precedent as bb_health_stack.c -- not a
// field in this struct.
//   ip         dotted-quad IPv4, populated ONLY on GOT_IP; zeroed otherwise.
//   disc_reason  meaningful on DISCONNECT/LOST_IP; BB_WIFI_DISC_UNKNOWN on
//                GOT_IP (see the evt/reason disambiguation note on
//                bb_wifi_net_event_t above -- key on the bb_event id
//                (edge), not disc_reason alone).
//
// NO RETAIN/REPLAY: bb_event core has no retain (replay is SSE-only). A
// consumer needing current state at subscribe time calls bb_wifi_has_ip() /
// bb_wifi_get_info() synchronously once, then subscribes.
typedef struct {
    char ip[16];
    bb_wifi_disc_reason_t disc_reason;
} bb_wifi_event_payload_t;

// Pure builder for bb_wifi_event_payload_t (KB 820, PR2 -- host-testable,
// bb_event-free). NULL-safe: no-op if out==NULL. Zeroes *out first, then
// sets disc_reason to a straight passthrough of `reason`. out->ip is
// populated ONLY when evt==BB_WIFI_NET_EVT_GOT_IP and ip!=NULL -- every
// other evt, or a NULL ip, leaves out->ip blank (this is the ip-blanking
// enforcement point for the wire contract above). Lives in bb_wifi (not the
// composition-level bridge) so it stays host-testable without pulling in
// bb_event; the intended caller is that bridge.
void bb_wifi_event_payload_build(bb_wifi_event_payload_t *out,
                                 bb_wifi_net_event_t evt,
                                 bb_wifi_disc_reason_t reason,
                                 const char *ip);

// Register a generic emit sink (bb_emit_fn, bb_core/bb_emit.h) for the
// wifi.net payload -- bus-shaped (topic, id, payload, size). NULL clears it.
// Fired on BB_WIFI_EVENT_TOPIC at the three STA lifecycle edges
// (got_ip/disconnect/lost_ip), with the id/payload built via
// bb_wifi_event_payload_build.
// Single-slot, single-consumer, null-safe: register once at init, before
// bb_wifi_init()/bb_wifi_init_sta(), from a single thread.
// bbtool:init tier=early fn=bb_wifi_set_emit consumes=emit_sink order=0
void bb_wifi_set_emit(bb_emit_fn cb);

// One-shot synthesis of the CURRENT STA state (bb_wifi_has_ip()/
// bb_wifi_get_info()) onto the generic emit slot, as a wifi.net GOT_IP or
// DISCONNECT payload. No-op if the slot is unset. Intended for a
// generic-seam consumer that registers via bb_wifi_set_emit() after boot
// and wants the current state without waiting for the next real edge -- it
// does NOT change the wifi.net contract's NO RETAIN/REPLAY guarantee for a
// bb_event subscriber (see above): that consumer still calls
// bb_wifi_has_ip()/bb_wifi_get_info() itself before subscribing.
void bb_wifi_emit_baseline(void);

